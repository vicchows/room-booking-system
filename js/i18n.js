  /**
   * Initialize language from localStorage or browser default
   */
  init() {
    const saved = localStorage.getItem('rbs_language');
    if (saved && this.translations[saved]) {
      this.currentLang = saved;
    } else {
      const browserLang = navigator.language || 'en';
      if (browserLang.startsWith('zh-TW') || browserLang.startsWith('zh-Hant')) {
        this.currentLang = 'zh-TW';
      } else if (browserLang.startsWith('zh')) {
        this.currentLang = 'zh-CN';
      } else if (browserLang.startsWith('th')) {
        this.currentLang = 'th';
      } else if (browserLang.startsWith('km')) {
        this.currentLang = 'km';
      } else if (browserLang.startsWith('fr')) {
        this.currentLang = 'fr';
      } else if (browserLang.startsWith('es')) {
        this.currentLang = 'es';
      } else {
        this.currentLang = 'en';
      }
    }
    this.applyToPage();
  },

  /**
   * Get translation for a key
   */
  t(key) {
    const lang = this.translations[this.currentLang];
    if (lang && lang[key] !== undefined) return lang[key];
    const en = this.translations['en'];
    if (en && en[key] !== undefined) {
      const warnKey = `${this.currentLang}:${key}`;
      if (this.currentLang !== 'en' && !this._warnedKeys.has(warnKey)) {
        console.warn(`[i18n] Missing key "${key}" for lang "${this.currentLang}", falling back to EN`);
        this._warnedKeys.add(warnKey);
      }
      return en[key];
    }
    const allKey = `all:${key}`;
    if (!this._warnedKeys.has(allKey)) {
      console.warn(`[i18n] Missing key "${key}" in all languages`);
      this._warnedKeys.add(allKey);
    }
    return key;
  },

  /**
   * Set language
   */
  setLanguage(lang) {
    if (this.translations[lang]) {
      this.currentLang = lang;
      localStorage.setItem('rbs_language', lang);
      this.applyToPage();
      document.dispatchEvent(new CustomEvent('languagechange', { detail: { lang } }));
    }
  },

  /**
   * Apply translations to all elements with data-i18n attribute
   */
  applyToPage() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translation = this.t(key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translation;
      } else if (el.tagName === 'OPTION') {
        el.textContent = translation;
      } else {
        el.textContent = translation;
      }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = this.t(el.getAttribute('data-i18n-placeholder'));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = this.t(el.getAttribute('data-i18n-title'));
    });
    document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
      el.setAttribute('aria-label', this.t(el.getAttribute('data-i18n-aria-label')));
    });
    // Update lang attribute on html
    document.documentElement.lang = this.currentLang;
    // Update active language in switcher
    document.querySelectorAll('.lang-option').forEach(el => {
      el.classList.toggle('active', el.getAttribute('data-lang') === this.currentLang);
    });
  },

